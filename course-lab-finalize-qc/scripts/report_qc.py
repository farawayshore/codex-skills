STEP_ID_RE = re.compile(r"\bP\d{2}\b")
PRINCIPLE_LABEL_RE = re.compile(r"\\label\{(fig:principle-[^}]+)\}")
ABSTRACT_RE = re.compile(r"\\begin\{abstract\}(.*?)\\end\{abstract\}", re.IGNORECASE | re.DOTALL)
TABLE_BLOCK_RE = re.compile(r"\\begin\{(?P<env>table\*?|longtable)\}(?P<body>.*?)\\end\{(?P=env)\}", re.DOTALL)
TABULAR_RE = re.compile(r"\\begin\{tabular\}(?:\[[^\]]*\])?\{(?P<spec>[^}]*)\}", re.DOTALL)
TABULARX_RE = re.compile(r"\\begin\{tabularx\}\{(?P<width>[^}]*)\}\{(?P<spec>[^}]*)\}", re.DOTALL)
